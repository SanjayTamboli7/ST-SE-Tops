#include <iostream>

using namespace std;

int main()
	{
		int a = 20, b = 10;
		cout << endl << "a              = " << a ;
		cout << endl << "b              = " << b ;
		cout << endl << "Addition       = " << (a+b);
		cout << endl << "Subtraction    = " << (a-b);
		cout << endl << "Multiplication = " << (a*b);
		cout << endl << "Division       = " << (float)(a/b);
		return 0;
	}